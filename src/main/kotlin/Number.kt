fun main() {
    //Integer Number
    var umur : Byte = 55;
    var nik : Long = 12072302039870003;

    //output Integer Number
    println("umur saya 30 tahun lagi adalah : " + umur);
    println("nik : " + nik);

    println("===========================================================================================")


    //Floating Point Number
    var nilai : Float = 80.5F;
    var waktu : Double = 07.55;

    //output Floatimg Point Number
    println("Nilai : " + nilai);
    println("sekarang oukul :" + waktu);

    println("===========================================================================================")


    //Literals
    var binar : Int = 0b0111000110;
    var campur : Int = 0xAB19CD;

    //output Literals
    println(binar);
    println(campur);

    println("===========================================================================================")

}